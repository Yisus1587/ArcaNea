export function detectSystemUiLang(): UiLang {
  try {
    const raw = (navigator.languages && navigator.languages.length ? navigator.languages[0] : navigator.language) || '';
    const s = String(raw).toLowerCase();
    if (s.startsWith('es')) return 'es';
    return 'en';
  } catch {
    return 'en';
  }
}

export function uiLangFromLocale(locale: string | null | undefined): UiLang {
  try {
    const s = String(locale || '').toLowerCase();
    if (s.startsWith('es')) return 'es';
    return 'en';
  } catch {
    return 'en';
  }
}

export function defaultMetadataLanguageForUiLang(lang: UiLang): string {
  // Provider metadata language used for TMDB localization.
  // Important: TMDB does not reliably accept `es-419`; prefer a concrete locale.
  return lang === 'es' ? 'es-MX' : 'en-US';
}

export function getStoredUiLang(): UiLang | null {
  try {
    const raw = localStorage.getItem(STORAGE_KEY);
    if (raw === 'es' || raw === 'en') return raw;
  } catch {
    // ignore
  }
  return null;
}

export function getStoredUiLangSource(): UiLangSource | null {
  try {
    const raw = localStorage.getItem(STORAGE_SOURCE_KEY);
    if (raw === 'system' || raw === 'manual') return raw;
  } catch {
    // ignore
  }
  return null;
}

export function getUiLang(): UiLang {
  const source = getStoredUiLangSource();
  if (source === 'manual') {
    return getStoredUiLang() ?? detectSystemUiLang();
  }
  if (source === 'system') {
    return detectSystemUiLang();
  }
  // Backward compatibility: if legacy storage exists, treat as manual.
  const legacy = getStoredUiLang();
  if (legacy) return legacy;
  return detectSystemUiLang();
}

export function setUiLang(lang: UiLang, source: UiLangSource = 'manual') {
  try {
    if (source === 'manual') {
      localStorage.setItem(STORAGE_KEY, lang);
      localStorage.setItem(STORAGE_SOURCE_KEY, 'manual');
    } else {
      localStorage.removeItem(STORAGE_KEY);
      localStorage.setItem(STORAGE_SOURCE_KEY, 'system');
    }
  } catch {
    // ignore
  }
  try {
    document.documentElement.lang = lang;
  } catch {
    // ignore
  }
}

type I18nContextValue = {
  lang: UiLang;
  setLang: (lang: UiLang, source?: UiLangSource) => void;
  t: (key: keyof (typeof STRINGS)['en']) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider(props: { children: React.ReactNode }) {
  const [lang, _setLang] = useState<UiLang>(() => getUiLang());

  const setLang = useCallback((next: UiLang, source: UiLangSource = 'manual') => {
    _setLang(next);
    setUiLang(next, source);
  }, []);

  useEffect(() => {
    // Ensure html lang is set on boot (even if stored value exists).
    setUiLang(lang, getStoredUiLangSource() ?? (getStoredUiLang() ? 'manual' : 'system'));
  }, [lang]);

  const t = useCallback((key: keyof (typeof STRINGS)['en']) => {
    return (STRINGS[lang] && STRINGS[lang][key]) ? STRINGS[lang][key] : STRINGS.en[key];
  }, [lang]);

  const value = useMemo<I18nContextValue>(() => ({ lang, setLang, t }), [lang, setLang, t]);

  return <I18nContext.Provider value={value}>{props.children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextValue {
  const v = useContext(I18nContext);
  if (!v) throw new Error('useI18n must be used within I18nProvider');
  return v;
}
